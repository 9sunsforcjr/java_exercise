    //这个按键控制暂停，开始or重启游戏
    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        if (ctrl.restart) {     //如果重启就先关线程，重设参数
            ctrl.shutThread = true;
            ctrl.temp.interrupt();
            ctrl.resetParameters();
        } else {
            ctrl.restart = true;
        }
        gp.setSize(200, 400);
        gp.setLocation(50, 50);
        bp.setSize(100, 80);
        bp.setLocation(290, 50);
        sp.setSize(80, 60);
        sp.setLocation(290, 160);

        Blocks block = new Blocks();
        Random random = new Random();           //设置首先出现的gp和bp里的物块
        ctrl.rand = random.nextInt(7);
        this.gp.block1 = block.graph[ctrl.rand][0];
        bp.repaint();
        this.bp.rand = random.nextInt(7);
        this.bp.block = block.graph[bp.rand][0];

        ctrl.resetColor();
        ctrl.shutThread = false;
        ctrl.runRepeatableThread();
    }//GEN-LAST:event_jButton2ActionPerformed

    //这个按键控制游戏速度，只能加速
