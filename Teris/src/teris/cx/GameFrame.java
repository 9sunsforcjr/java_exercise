    //这个按键控制游戏速度，只能减速，而且不能低于当前等级的最低速度
    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        if ((sp.score < 10 && sp.gameSpeed > 0) || (sp.score < 20 && sp.gameSpeed > 1) || (sp.score < 30 && sp.gameSpeed > 4)) {
            --sp.gameSpeed;
        }
        sp.repaint();
    }//GEN-LAST:event_jButton3ActionPerformed

    //把按钮属性的focusable取消就可以避免焦点在按钮上的问题了
    private void formKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyPressed
        // TODO add your handling code here:
        if (!ctrl.pause) {
            switch (evt.getKeyCode()) {
                case 37:
                    if (ctrl.isOverstep(gp.x - 20, gp.y, gp.block1)) {
                        break;
                    }
                    ctrl.left();
                    break;
                case 39:
                    if (ctrl.isOverstep(gp.x + 20, gp.y, gp.block1)) {
                        break;
                    }
                    ctrl.right();
                    break;
                case 40:
                    if (ctrl.downIsOverstep(gp.x, gp.y + 20, gp.block1)) {
                        break;
                    }
                    ctrl.down();
                    break;
                case 38:
                    ctrl.turn();
                    break;
            }
        }
    }//GEN-LAST:event_formKeyPressed

